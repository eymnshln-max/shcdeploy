import type { PatientProfile, RiskMods } from "../types";

export function computeRiskProfile(profile: PatientProfile): { mods: RiskMods; bmiCat: string } {
  const {age,sex,bmi,smoking,diabetes,hypertension,familyHistory}=profile;
  const mods: RiskMods = {};
  const bmiCat=!bmi?"unknown":bmi<18.5?"underweight":bmi<25?"normal":bmi<30?"overweight":bmi<35?"obese_1":"obese_2";
  const obese = bmiCat==="obese_1"||bmiCat==="obese_2";
  const female = sex==="female";
  const male = sex==="male";
  const hasAge = typeof age === "number";
  const ageGt = (value: number) => hasAge && age > value;
  const ageLt = (value: number) => hasAge && age < value;
  const ageBetween = (min: number, max: number) => ageGt(min) && ageLt(max);
  const older = ageGt(60); const middle = ageGt(40); const young = ageLt(35);

  mods.diabetes_t2=1.0*(bmiCat==="obese_2"?3.2:bmiCat==="obese_1"?2.1:bmiCat==="overweight"?1.5:1.0)*(ageGt(45)?1.8:ageGt(35)?1.3:1.0)*(familyHistory?1.6:1.0)*(smoking?1.3:1.0);
  mods.diabetes_t1=1.0*(young?2.0:1.0)*(familyHistory?2.0:1.0);
  mods.hypothyroidism=1.0*(female?5.0:1.0)*(older?1.8:1.0);
  mods.hyperthyroidism=1.0*(female?7.0:1.0)*(ageBetween(20,50)?1.5:1.0);
  mods.cushings=1.0*(female?3.0:1.0)*(obese?1.5:1.0)*(ageBetween(20,50)?1.5:1.0);
  mods.addisons=1.0*(female?2.0:1.0)*(ageBetween(20,50)?1.5:1.0);
  mods.pcos=female&&ageLt(50)?1.0*(bmiCat==="obese_1"||bmiCat==="obese_2"?1.8:1.0)*(ageBetween(13,45)?2.0:0.1):0.0;
  mods.acs=1.0*(male?1.8:1.0)*(ageGt(65)?3.0:ageGt(50)?2.0:ageGt(40)?1.4:0.6)*(diabetes?2.0:1.0)*(smoking?1.9:1.0)*(hypertension?1.6:1.0)*(familyHistory?1.5:1.0);
  mods.aortic_dissection=1.0*(male?2.0:1.0)*(ageGt(60)?2.5:ageGt(50)?1.6:1.0)*(hypertension?2.5:1.0);
  mods.heart_failure=1.0*(ageGt(65)?2.5:ageGt(50)?1.5:1.0)*(diabetes?1.8:1.0)*(hypertension?2.0:1.0)*(obese?1.6:1.0);
  mods.pe=1.0*(ageGt(50)?1.5:1.0)*(obese?1.4:1.0)*(smoking?1.3:1.0);
  mods.stroke=1.0*(ageGt(65)?2.5:ageGt(50)?1.6:1.0)*(hypertension?2.1:1.0)*(diabetes?1.5:1.0)*(smoking?1.7:1.0);
  mods.hypertensive_crisis=1.0*(ageGt(50)?1.8:1.0)*(hypertension?4.0:1.0)*(diabetes?1.4:1.0);
  mods.afib=1.0*(ageGt(65)?3.0:ageGt(55)?1.8:1.0)*(hypertension?1.8:1.0)*(diabetes?1.4:1.0);
  mods.dvt=1.0*(ageGt(50)?1.5:1.0)*(obese?1.5:1.0)*(smoking?1.3:1.0);
  mods.parkinsons=1.0*(ageGt(70)?3.0:ageGt(60)?2.0:ageGt(50)?1.2:0.3)*(male?1.5:1.0);
  mods.essential_tremor=1.0*(ageGt(60)?2.0:ageGt(40)?1.3:1.0)*(familyHistory?2.0:1.0);
  mods.migraine=1.0*(female?3.0:1.0)*(ageBetween(15,55)?1.5:0.8);
  mods.ms=1.0*(female?2.5:1.0)*(ageBetween(20,40)?2.0:0.8);
  mods.epilepsy=1.0*(ageLt(30)?1.5:1.0);
  mods.tension_headache=1.0*(female?1.3:1.0)*(middle?1.2:1.0)*(smoking?1.2:1.0);
  mods.lupus=1.0*(female?9.0:1.0)*(ageBetween(15,50)?2.0:0.6);
  mods.ra=1.0*(female?2.5:1.0)*(ageBetween(40,70)?1.8:1.0)*(smoking?1.5:1.0)*(familyHistory?1.6:1.0);
  mods.gout=1.0*(male?4.0:1.0)*(ageGt(40)?2.0:1.0)*(obese?1.8:1.0)*(diabetes?1.5:1.0);
  mods.fibromyalgia=1.0*(female?7.0:1.0)*(ageBetween(30,60)?1.5:1.0);
  mods.osteoporosis=1.0*(female?3.0:1.0)*(ageGt(65)?3.0:ageGt(50)?1.5:0.3)*(bmiCat==="underweight"?2.0:1.0);
  mods.appendicitis=1.0*(ageBetween(10,40)?2.0:1.0);
  mods.ibd=1.0*(ageBetween(15,40)?1.8:1.0)*(smoking?1.5:1.0)*(familyHistory?2.0:1.0);
  mods.celiac=1.0*(female?1.5:1.0)*(familyHistory?3.0:1.0);
  mods.peptic_ulcer=1.0*(male?1.5:1.0)*(smoking?2.0:1.0)*(older?1.5:1.0);
  mods.gerd=1.0*(obese?2.5:1.0)*(smoking?1.5:1.0)*(older?1.3:1.0);
  mods.bowel_obstruction=1.0*(older?2.0:1.0);
  mods.cholecystitis=1.0*(female?2.0:1.0)*(obese?2.5:1.0)*(ageGt(40)?1.8:1.0);
  mods.pancreatitis=1.0*(male?1.5:1.0)*(ageGt(40)?1.5:1.0);
  mods.ibs=1.0*(female?2.0:1.0)*(ageBetween(15,50)?1.5:1.0);
  mods.nafld=1.0*(bmiCat==="obese_2"?3.5:bmiCat==="obese_1"?2.2:bmiCat==="overweight"?1.5:0.5)*(diabetes?2.0:1.0)*(ageGt(40)?1.4:1.0);
  mods.copd=1.0*(smoking?4.0:1.0)*(ageGt(50)?2.0:1.0);
  mods.asthma=1.0*(female?1.2:1.0)*(young?1.5:1.0);
  mods.pneumonia=1.0*(older?2.5:1.0)*(smoking?1.5:1.0)*(diabetes?1.5:1.0);
  mods.depression=1.0*(female?1.8:1.0)*(older?1.3:1.0)*(smoking?1.3:1.0);
  mods.anxiety=1.0*(female?2.0:1.0)*(young?1.5:1.0)*(smoking?1.2:1.0);
  mods.bipolar=1.0*(ageBetween(15,35)?1.8:1.0)*(familyHistory?2.5:1.0);
  mods.ptsd=1.0*(female?2.0:1.0);
  mods.ckd=1.0*(diabetes?2.5:1.0)*(hypertension?2.0:1.0)*(ageGt(60)?1.8:1.0);
  mods.kidney_stones=1.0*(male?2.0:1.0)*(ageBetween(20,60)?1.8:1.0)*(obese?1.4:1.0);
  mods.uti=1.0*(female?10.0:1.0)*(diabetes?2.0:1.0)*(older?1.5:1.0);
  mods.pyelonephritis=1.0*(female?5.0:1.0)*(diabetes?2.5:1.0);
  mods.anemia=1.0*(female?2.5:1.0)*(older?1.5:1.0);
  mods.sleep_apnea=1.0*(bmiCat==="obese_2"?4.0:bmiCat==="obese_1"?2.5:bmiCat==="overweight"?1.6:1.0)*(male?2.0:1.0)*(ageGt(40)?1.5:1.0);
  mods.cancer_red_flags=1.0*(ageGt(50)?2.5:ageGt(40)?1.5:1.0)*(smoking?1.8:1.0)*(familyHistory?1.8:1.0);
  mods.ectopic=female&&ageBetween(12,50)?1.0:0.0;
  mods.sepsis=1.0*(older?2.0:1.0)*(diabetes?1.8:1.0);
  mods.covid=1.0*(older?2.5:1.0)*(obese?1.5:1.0)*(diabetes?1.6:1.0);
  mods.influenza=1.0*(older?2.0:young?1.3:1.0)*(obese?1.3:1.0);
  mods.lumbar_radiculopathy=1.0*(male?1.3:1.0)*(ageBetween(30,65)?1.8:1.0)*(obese?1.4:1.0);
  mods.cauda_equina=1.0*(ageBetween(30,60)?1.5:1.0);
  mods.cellulitis=1.0*(diabetes?2.5:1.0)*(obese?1.5:1.0)*(older?1.4:1.0);
  mods.psoriasis=1.0*(ageBetween(15,40)?1.5:1.0)*(familyHistory?2.5:1.0)*(smoking?1.4:1.0);
  mods.acute_glaucoma=1.0*(ageGt(50)?2.5:1.0)*(female?1.3:1.0);

  return{mods,bmiCat};
}
